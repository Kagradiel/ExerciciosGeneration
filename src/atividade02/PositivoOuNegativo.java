package atividade02;

public class PositivoOuNegativo {

	public void positivoOuNegativo(int numero){
		
		if(numero > 0) {
			System.out.println("O numero é positivo");
		}else if(numero < 0 ) {
			System.out.println("O Numero é negativo");
		}else {
			System.out.println("Numero zero.");
		}
		
	}
}
